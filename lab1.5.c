#include <stdio.h>

int main() {
    int num;
    int evenSum = 0;
    int oddSum = 0;

    printf("กรุณาป้อนจำนวนเต็ม (ป้อน 0 เพื่อหยุด):\n");

    // 1. รับค่าตัวเลขแรกก่อนเข้าสู่ลูป
    if (scanf("%d", &num) != 1) {
        // หากรับค่าแรกไม่ได้ ก็ออกจากโปรแกรม
        return 1; 
    }
    
    // โค้ดที่หายไป: ต้องบวกค่าแรกที่รับมาด้วย
    if (num != 0) { // ตรวจสอบไม่ให้บวก 0 หากผู้ใช้ป้อน 0 เป็นค่าแรก
        if (num % 2 == 0) {
            evenSum += num; 
        } else {
            oddSum += num; 
        }
    }


    // 2. ลูปจะทำงานตราบใดที่ค่า num ที่ถูกบวกไปแล้ว ไม่ใช่ 0
    while (num != 0 ) {

        // รับค่าตัวเลขใหม่ (ตัวที่ 2, 3, ...) ภายในลูป
        if (scanf("%d", &num) != 1) {
            // หากรับค่าไม่ได้ (เช่น ป้อนตัวอักษร) ก็ออกจากลูป
            break; 
        }
        
        // ตรวจสอบว่าค่าที่รับมาใหม่นี้ไม่ใช่ 0
        if (num != 0) {
            // 3. ตรวจสอบและบวกค่าที่รับมาใหม่เข้ากับผลรวม
            if (num % 2 == 0) {
                evenSum += num; 
            } else {
                oddSum += num; 
            }
        }
        // ถ้า num เป็น 0 ลูปจะหยุดเองในรอบถัดไป
    }

    // 4. แสดงผลลัพธ์ (ต้องอยู่ในขอบเขตของ main {})
    printf("\n--- ผลลัพธ์ ---\n");
    printf("Even Sum: %d\n", evenSum);
    printf("Odd Sum: %d\n", oddSum);
    
    return 0; // คืนค่า 0 เมื่อโปรแกรมจบอย่างสมบูรณ์
}